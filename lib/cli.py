from colorama import Fore, Style

long_line_ascii_art = "\033[2m" + Fore.WHITE + """
★・・・・・・・★・・・・・・・・★・・・・・・・★・・・・・・・★
""" + Style.RESET_ALL


def show_exit_art():
    """Display the exit ASCII art with color."""
    print(exit_ascii_art)
    print(Fore.GREEN + "I hope I brought some cheer to your day. If not, that's okay — remember, brighter days are ahead. See you soon!")
    print(long_line_ascii_art)

def main():
    print(start_app_ascii_art)  # Display starting ASCII art
    while True:
        menu()
        choice = input(Fore.YELLOW + "> " + Style.RESET_ALL)
        if choice == "1":
            log_mood()  # Log your mood
        elif choice == "2":
            view_moods()
        elif choice == "3":
            get_quote()  # Get a motivational quote
        elif choice == "4":
            get_animal_story()  # Learn an inspiring animal story
        elif choice == "0":
            show_exit_art()  # Show exit ASCII art
            break
        else:
            print(Fore.RED + "Invalid choice. Please try again.")

